#here we declare the complex
complex_number_1=23+45j
complex_number_2=25+4j
#here the format is a+bj
#complex numbers can be added,substracted and conducted other functions too
print("the complex we declared is : ",complex_number_1)
#adding the complex number
print("the sum is:",complex_number_1+complex_number_2)
#complex number have 2 parts i.e : imaginary and real part
#to print the real part
print("the real part is : ",complex_number_1.real)
print("the imaginary part is : ",complex_number_1.imag)
