# Printing in Python:
# The print() function is used to display output on the screen.

# 1. Print a simple message:
print("Hello, world!")  # prints: Hello, world!

# 2. Print a message with a variable using concatenation:
name = "Alice"
print("Hello, " + name)  # prints: Hello, Alice

# 3. Print multiple values using commas:
age = "25"
print("Name:", name, "Age:", age)  
# prints: Name: Alice Age: 25

# 4. Print a message and a string variable only (no calculations):
message = "Welcome to Python!"
print(message)  # prints: Welcome to Python!
