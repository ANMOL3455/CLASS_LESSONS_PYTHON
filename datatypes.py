# Data Types in Python:
# Python has several built-in data types to store different kinds of values.

# 1. Integer (int):
# Represents whole numbers without a decimal point.
# Example:
age = 25        # 'age' is an integer variable storing 25

# 2. Float:
# Represents numbers with decimal points (floating-point numbers).
# Example:
height = 5.9    # 'height' is a float variable storing 5.9

# 3. String (str):
# Represents a sequence of characters, enclosed in single or double quotes.
# Example:
name = "Alice"  # 'name' is a string variable storing "Alice"

# You can perform operations on these types:
# - int + int = int
# - int + float = float
# - str + str = concatenation of strings
